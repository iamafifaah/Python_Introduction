# Celcius
celcius = float(input('Masukkan suhu dalam celcius : ',))
print('suhu adalah ', celcius, '째 Celcius')

# Reamur
reamur = (4/5)* celcius
print('suhu adalah ', reamur, '째 Reamur')

# Fahrenheit
Fahrenheit = ((9/5)* celcius)+32
print('suhu adalah ', Fahrenheit, '째 Fahrenheit')

# Kelvin
Kelvin = celcius + 273
print('suhu adalah ', Kelvin, '째 Kelvin')


